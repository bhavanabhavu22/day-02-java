public class comparisionoperator {
    public static void main(String args[])
    {
        int a=8,b=10;
        
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        
    }
}
